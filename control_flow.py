# If,else statements - Practice
age =15
# The following statement checks whether the age variable is greater than 15 and prints the statement "Thank you, you can watch the movie"
if age>15:
    print("Thank you, you can watch the movie")
# If the user is not older than 15, the elif statement is enacted.
elif age<=15:
    print("Oh no, you are too young to watch this movie")
else:
    print("oops, something went wrong, try again later")

